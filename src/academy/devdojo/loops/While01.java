package academy.devdojo.loops;

public class While01 {
    public static void main(String[] args) {
        int counter = 0;
        while(counter < 10){
            System.out.println(counter + " HEEEEEEEY DEVDOJO!");
            counter = counter + 1;
        }
    }
}

// [ 1 ] = 0 + 1
// [ 2 ] = 1 + 1
// [ 3 ] = 2 + 1
