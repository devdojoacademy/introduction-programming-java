/*
Create a class called ExerciseAgeDifference
Create a variable that can store the first age and assign some value
Create a second variable to store the second age and also assign some value
Create a third variable that will be the difference between first and second age
Print in the console all the ages and the difference
*/

public class ExerciseAgeDifference {
    public static void main(String[] args) {
        int firstAge = 40;
        int secondAge = 10;
        int ageDifference = firstAge - secondAge;
        System.out.println("First Age: " +firstAge);
        System.out.println("Second Age: " +secondAge);
        System.out.println("Age difference: " +ageDifference);
    }

}
